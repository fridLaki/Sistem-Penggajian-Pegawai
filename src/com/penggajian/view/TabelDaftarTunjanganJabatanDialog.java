    private void refreshTable(){
        MasterService masterService = SpringManager.getInstance().getBean(MasterService.class);
        dynamicTableModel.clear();
        for (TabelTunjanganJabatan tj : masterService.getAllTunjanganJabatan()) {
            dynamicTableModel.add(tj);
        }
        cmbJabatan.setSelectedIndex(0);
        txtJumlahTunjangan.setText("");
        cmbGolongan.setSelectedIndex(0);
        cmbJumlahRobengan.setSelectedIndex(0);
        btnSimpan.setText("Simpan");
    }
    
    private void addListSelectionListener(){
        dynamicTable.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (dynamicTable.getSelectedRow() != -1) {
                    btnSimpan.setText("Ubah");
                    ttj = dynamicTableModel.get(
                            dynamicTable.convertRowIndexToModel(
                            dynamicTable.getSelectedRow()));
                    try {
                        cmbJabatan.setSelectedItem(ttj.getJabatan());
                        cmbGolongan.setSelectedItem(ttj.getGolongan());
                        cmbJumlahRobengan.setSelectedItem(ttj.getJumlahRombengan());
                        txtJumlahTunjangan.setText(TextComponentUtils.formatNumber(String.valueOf(ttj.getJumlahTunjangan())));
                    } catch (Exception ex) {
                    }
                }
            }
        });
    }
