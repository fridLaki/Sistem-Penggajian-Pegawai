    private void buttonAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAction
       // TODO add your handling code here:
        Object source = evt.getSource();
        if(source == btnSimpan){
            if (btnSimpan.getText().matches("Simpan")) {
                ttj = new TabelTunjanganJabatan();
                ttj.setJabatan(cmbJabatan.getSelectedItem().toString().trim());
                ttj.setGolongan(cmbGolongan.getSelectedItem().toString().trim());
                ttj.setJumlahRombengan(cmbJumlahRobengan.getSelectedItem().toString().trim());
                ttj.setJumlahTunjangan(BigDecimal.valueOf(Long.valueOf(txtJumlahTunjangan.getText().replace(".", "").trim())));
                
                TabelTunjanganJabatanValidator validator = SpringManager.getInstance().getBean(TabelTunjanganJabatanValidator.class);
                MasterService masterService = SpringManager.getInstance().getBean(MasterService.class);
                
                try {
                    validator.validate(ttj);
                    masterService.save(ttj);
                    showWarning("Simpan sukses");
                    refreshTable();
                } catch (DataAccessException ex) {
                    showError(ex.getRootCause().getMessage());
                } catch (ValidatorException e) {
                    showWarning(e.getMessage());
                }
            } else if(btnSimpan.getText().matches("Ubah")) {
                ttj.setJabatan(cmbJabatan.getSelectedItem().toString().trim());
                ttj.setGolongan(cmbGolongan.getSelectedItem().toString().trim());
                ttj.setJumlahRombengan(cmbJumlahRobengan.getSelectedItem().toString().trim());
                ttj.setJumlahTunjangan(BigDecimal.valueOf(Long.valueOf(txtJumlahTunjangan.getText().replace(".", "").trim())));

                TabelTunjanganJabatanValidator validator = SpringManager.getInstance().getBean(TabelTunjanganJabatanValidator.class);
                MasterService masterService = SpringManager.getInstance().getBean(MasterService.class);

                try {
                    validator.validate(ttj);
                    masterService.update(ttj);
                    showWarning("Ubah sukses");
                    refreshTable();
                } catch (DataAccessException ex) {
                    showError(ex.getRootCause().getMessage());
                } catch (ValidatorException e) {
                    showWarning(e.getMessage());
                }
            }
        } else if(source == btnHapus){
            if (dynamicTable.getSelectedRow() == -1) {
                showWarning("Silahkan pilih salah satu");
            } else {
                TabelTunjanganJabatan tj = dynamicTableModel.get(
                        dynamicTable.convertRowIndexToModel(
                        dynamicTable.getSelectedRow()));
                
                MasterService masterService = SpringManager.getInstance().getBean(MasterService.class);
                
                try {
                    masterService.delete(tj);
                    refreshTable();
                } catch (DataAccessException ex) {
                    showError(ex.getRootCause().getMessage());
                }
            }  
        } else if (source == btnBatal){
            dispose();
        }
    }//GEN-LAST:event_buttonAction

